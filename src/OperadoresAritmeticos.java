
public class OperadoresAritmeticos {

    public static void main(String[] args) {
        int num1 = 7;
        
        System.out.println("La división con / es:"+ num1/2);
        System.out.println("La división con % es:"+ num1%2);
        
        if (num1%2 == 0) {
            System.out.println(num1 + " es PAR");
        } else {
            System.out.println(num1 + " es IMPAR");
        }
    }
    
    
}
